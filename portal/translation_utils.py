def translate_text(value, language_code):
    if not isinstance(value, str):
        return value
    language = (language_code or "en").split("-")[0]
    if language == "en":
        return value
    return TRANSLATIONS.get(language, {}).get(value, value)


def localize_object(instance, language_code):
    if instance is None:
        return instance
    localized_instance = copy(instance)
    for key, value in vars(instance).items():
        if key.startswith("_"):
            continue
        if isinstance(value, str):
            setattr(localized_instance, key, translate_text(value, language_code))
    return localized_instance


def localize_collection(items, language_code):
    localized = []
    for item in items:
        localized.append(localize_object(item, language_code))
    return localized
